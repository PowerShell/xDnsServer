                $resourceCurrentState.Ensure | Should -Be $ZoneData.Ensure
                $resourceCurrentState.Name | Should -Be $ZoneData.ZoneName
                $resourceCurrentState.MasterServers | Should -Be $ConfigurationData.NonNodeData.MasterServers
